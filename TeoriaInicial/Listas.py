#Creamos nueva lista(arrays en otros lenguajes pero con sus caracteristicas especiales)
#Pueden ser de diferentes tipos de datos
nuevaLista=["Juan",True,'a',12.56,4,'Pablo']
#Mostramos la lista
print(nuevaLista) #mostrar toda la lista
#mostramos un elemento de la lista
print(nuevaLista[2])
#mostramos un rango de la lislta
print(nuevaLista[0:3])
print(nuevaLista[:2]) #desde de la posicion 0 hasta el segundo elemento(no posicion)
print(nuevaLista[2:4])
print(nuevaLista[2:])  #desde de la posicion 2 hasta el quinto elemento
print(nuevaLista[-2]) #posiciones negativas cuantas de manera negativa desde la ultima posicion





